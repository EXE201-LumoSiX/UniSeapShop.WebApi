namespace UniSeapShop.Domain.Enums;

public enum ProductCondition
{
    BrandNew,    // Unopened, unused item
    LikeNew,     // Used briefly, in excellent condition
    Good,        // Used but well-maintained, minimal wear
    Fair,        // Shows signs of regular use
    Poor,        // Significant wear, may need repairs
    ForParts     // Not fully functional, useful for parts
}